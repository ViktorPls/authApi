const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    min: 6,
    max: 255,
  },
  email: {
    type: String,
    required: true,
    min: 6,
    max: 255,
  },
  password: {
    type: String,
    required: true,
    min: 6,
    max: 1024,
  },
  salt: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
    //TODO: CAmbiar fecha para tenga formate mas legible tipo toLocaleString()
  },
});

module.exports = mongoose.model("User", userSchema);
